package 来换网;

public class goods {
private String goodsid;  //物品ID
private String goodsname;//物品名字
private String userid;    //所属用户名
private String style;    //物品类型
private String exchangestyle;  //想交换物品类型
private String picture;    //图片
public String getGoodsid() {
	return goodsid;
}
public void setGoodsid(String goodsid) {
	this.goodsid = goodsid;
}
public String getGoodsname() {
	return goodsname;
}
public void setGoodsname(String goodsname) {
	this.goodsname = goodsname;
}
public String getUserid() {
	return userid;
}
public void setUserid(String userid) {
	this.userid = userid;
}
public String getStyle() {
	return style;
}
public void setStyle(String style) {
	this.style = style;
}
public String getExchangestyle() {
	return exchangestyle;
}
public void setExchangestyle(String exchangestyle) {
	this.exchangestyle = exchangestyle;
}
public String getPicture() {
	return picture;
}
public void setPicture(String picture) {
	this.picture = picture;
}

}
